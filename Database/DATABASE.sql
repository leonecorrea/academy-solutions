USE ACADEMYSOLUTIONS;

CREATE TABLE TblAccount(
	CodAccount INT IDENTITY(1,1000),--PK
	FirstName VARCHAR(10),
	LastName VARCHAR(10),
	NameFull VARCHAR (50),
	DateBirth DATETIME,
	DateRegister DATETIME,
	Address VARCHAR (255),
	CodCategory INT,--FK
	CONSTRAINT PK_ACCOUNT PRIMARY KEY (CodAccount)
)

